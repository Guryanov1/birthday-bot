import random

class BirthdayAssistant:
    def __init__(self):
        pass

    async def generate_congratulation(self, name: str, age: int = None):
        congratulations = [
            f"🎉 Дорогой(ая) {name}!\n\nОт всего сердца поздравляю тебя с днём рождения! Пусть этот новый год жизни будет наполнен яркими событиями, тёплыми встречами и исполнением всех твоих желаний. Желаю крепкого здоровья, огромного счастья и чтобы рядом всегда были люди, которые тебя любят и поддерживают.",
            
            f"🎂 Поздравляю тебя, {name}! 🎂\n\nСегодня твой день! Желаю, чтобы жизнь дарила тебе только приятные сюрпризы, чтобы здоровье было железным, настроение отличным, а все мечты сбывались быстро и легко. Ты очень хороший человек, и я рад, что ты есть!",
            
            f"🎉 С днём рождения, дорогой(ая) {name}!\n\nПусть этот год станет для тебя особенным — полным успехов, радости и новых возможностей. Желаю любви, гармонии, финансового благополучия и чтобы все задуманное обязательно реализовалось. Будь счастлив!",
            
            f"🎂 {name}, с днём рождения тебя!\n\nПусть в твоей жизни будет больше причин улыбаться, меньше поводов грустить и много-много тёплых моментов. Здоровья тебе, удачи во всех делах и чтобы следующий год был лучше предыдущего!"
        ]
        
        if age:
            age_text = f"\n\nСегодня тебе {age} лет — это прекрасный возраст для новых достижений!"
            return random.choice(congratulations) + age_text
        
        return random.choice(congratulations)